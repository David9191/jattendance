/**
 * 랜덤으로 성경 말씀을 선택하는 함수. 아래 예시
 * {
 *   verse: '여호와는 나의 목자시니 내게 부족함이 없으리로다',
 *   book: '시편',
 *   chapter: 23,
 *   verseNumber: 1,
 *   version: '개역개정',
 * },
 * @returns
 */
export function getRandomVerse() {
  const randomIndex = Math.floor(Math.random() * bibleVerses.length);
  return bibleVerses[randomIndex];
}

// 특정 번역본의 말씀만 가져오는 함수
export function getVersesByVersion(version) {
  return bibleVerses.filter((verse) => verse.version === version);
}

// 랜덤으로 특정 번역본의 말씀을 선택하는 함수
export function getRandomVerseByVersion(version) {
  const versionVerses = getVersesByVersion(version);
  const randomIndex = Math.floor(Math.random() * versionVerses.length);
  return versionVerses[randomIndex];
}
